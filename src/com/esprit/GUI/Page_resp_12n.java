    private void stat_btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stat_btn2ActionPerformed
  p.hide();
        jPanel5.hide();
         jPanel3.hide();
 jPanel4.hide();
 jPanel6.hide();
      
        try {
            DefaultPieDataset PieDataset = new DefaultPieDataset();
            ProduitDao md = new ProduitDao();
            List<Produit> moyens = new Vector<>();
            moyens = md.getProduitsGroupByStocknour();

            for (int i = 0; i < moyens.size(); i++) {
                // System.out.println(moyens.get(i).getCategories1());
                PieDataset.setValue(moyens.get(i).getCategories(), md.countnour(moyens.get(i).getId()));
            }
            JFreeChart chart = ChartFactory.createPieChart3D("Répartition des produits selon catégorie", PieDataset, true, true, true);
            chart.setBackgroundPaint((new Color(0xFFFFFF)));//********

            PiePlot p = (PiePlot) chart.getPlot();
            p.setBackgroundPaint((new Color(0xFFE4C4)));//************
            //p.setForegroundAlpha(TOP_ALIGNMENT);

            ChartPanel frame = new ChartPanel(chart);
            frame.setVisible(true);
            frame.setSize(700, 480);
            panel_stat.add(frame);

        } catch (SQLException ex) {
            Logger.getLogger(fos_user.class.getName()).log(Level.SEVERE, null, ex);
        }
        panel_image_deroulante.setVisible(false);
        p.setVisible(false);
        
        panel_stat.setVisible(true);
       
    }//GEN-LAST:event_stat_btn2ActionPerformed

