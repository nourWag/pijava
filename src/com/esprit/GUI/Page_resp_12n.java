    private void addImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addImageActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f =chooser.getSelectedFile();
        String filename2 = null;
        filename2 = f.getName();
        String filename =f.getAbsolutePath().toString();
        ImageIcon c= new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(jLImage.getWidth(),jLImage.getHeight() , Image.SCALE_DEFAULT));

        jLImage.setIcon(c);
        g9.setText(filename2);
        Image  img =c.getImage();

        BufferedImage bufferedImage = new BufferedImage(c.getIconHeight(),c.getIconWidth(),BufferedImage.TYPE_INT_RGB);
        //BufferedImage bimage = new BufferedImage(img.getWidth(null), img.getHeight(null), BufferedImage.TYPE_INT_ARGB);

        Graphics g = bufferedImage.createGraphics();
        g.drawImage(img, 0, 0, null);
        g.dispose();
        String s=getClass().getResource("/com/esprit/image/").toString();
        s=s.substring(5, s.length());
        s=s.substring(0, s.length()-31);
        s=s+"src/com/esprit/image/";
        try {
            //getClass().getResource("/com/esprit/image/add_256 copie.png")
            System.out.println(s+filename2);
            ImageIO.write(bufferedImage, "jpg",new File(s+filename2));
        } catch (IOException ex) {
            Logger.getLogger(Page_resp_11.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addImageActionPerformed

